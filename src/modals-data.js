export function makeTaskModalFields(task){
    const MODAL_FIELDS = {
        title: {
            wrapper: {
                classes: ["title-wrapper"]
            },
            label: {
                innerText: "Title"
            },
            input: {
                htmlElement: "input",
                attributes: {
                    type: "text",
                    id: "title",
                    maxLength: 30,
                    "data-modal-name": "title"
                },
                selfValidates: true,
                required: true
            },
        },
        description: {
            wrapper: {
                classes: ["description-wrapper"]
            },
            label: {
                innerText: "Description"
            },
            input: {
                htmlElement: "input",
                attributes: {
                    type: "textarea",
                    id: "description",
                    "data-modal-name": "description"
                }
            },
            viewMode: {
                container: {
                    classes: ["description-container"]
                },
                text: {
                    innerHTML: task && `<span>Description: </span> ${task.description}`
                }
            }
        },
        dueDate: {
            wrapper: {
                classes: ["due-date-wrapper"]
            },
            label: {
                innerText: "Due date"
            },
            input: {
                htmlElement: "input",
                attributes: {
                    type: "datetime-local",
                    id: "dueDate",
                    "data-modal-name": "due date"
                },
                selfValidates: true,
                required: true
            },
            viewMode: {
                container: {
                    classes: ["due-date-container"]
                },
                text: {
                    innerHTML: task && `<span>Due date: </span> ${task.formattedDate}`
                }
            }
        },
        priority: {
            wrapper: {
                classes: ["priority-wrapper"]
            },
            label: {
                innerText: "Priority"
            },
            input: {
                htmlElement: "select",
                options: ["low", "medium", "high"],
                attributes: {
                    id: "priority",
                    "data-modal-name": "priority"
                },
            },
            viewMode: {
                container: {
                    classes: ["priority-container"]
                },
                text: {
                    innerHTML: task && `<span>Priority: </span> ${task.priority}`
                }
            }
        },
        project: {
            wrapper: {
                classes: ["project-wrapper"]
            },
            label: {
                innerText: "Project"
            },
            input: {
                htmlElement: "select",
                options: getProjectNames(),
                attributes: {
                    id: "project",
                    "data-modal-name": "project"
                },
            },
            viewMode: {
                container: {
                    classes: ["project-container"]
                },
                text: {
                    innerHTML: task && `<span>Project: </span> ${task.project}`
                }
            }
        },
    }
    return MODAL_FIELDS;
}